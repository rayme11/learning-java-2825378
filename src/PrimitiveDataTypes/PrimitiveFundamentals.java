package PrimitiveDataTypes;

// There are 4 main Primivite Data Types
// Boolean, int, double, and char

// The rest are the Reference Types

public class PrimitiveFundamentals {

    public static void main(String[] args){

        int i = 0;
        boolean yes = true;
        char myChar = 'C';
        double myDouble = 78.99;

        System.out.println("int value is: " + i);
        System.out.println("boolean value : " + yes);
        System.out.println("char value : " + myChar);
        System.out.println("double value is: " + myDouble);


    }
}
